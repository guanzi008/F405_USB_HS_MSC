static const uint8_t *wouo_get_glyph(char ch)
{
    if ((ch < ' ') || (ch > '~')) {
        ch = '?';
    }
    return s_wouo_f6x8[(uint8_t)ch - (uint8_t)' '];
}

static void wouo_draw_char_6x8(uint8_t x, uint8_t y, char ch, uint8_t fg, uint8_t bg)
{
    const uint8_t *glyph = wouo_get_glyph(ch);
    uint8_t col;
    uint8_t row;

    ls013_lcd_rect(x, y, WOUO_FONT_W, WOUO_FONT_H, bg);
    for (col = 0u; col < WOUO_FONT_W; ++col) {
        uint8_t bits = glyph[col];
        for (row = 0u; row < WOUO_FONT_H; ++row) {
            if (((bits >> row) & 0x01u) != 0u) {
                ls013_lcd_set_pixel((uint8_t)(x + col), (uint8_t)(y + row), fg);
            }
        }
    }
}

void wouo_draw_text_6x8(uint8_t x, uint8_t y, uint8_t clear_w, const char *text, uint8_t fg, uint8_t bg)
{
    uint8_t draw_x = x;
    uint8_t max_x;

    if (text == NULL) {
        text = "";
    }

    ls013_lcd_rect(x, y, clear_w, WOUO_FONT_H, bg);
    if (clear_w < WOUO_FONT_W) {
        return;
    }
    max_x = (uint8_t)(x + clear_w - WOUO_FONT_W);
    while ((*text != '\0') && (draw_x <= max_x)) {
        wouo_draw_char_6x8(draw_x, y, *text, fg, bg);
        draw_x = (uint8_t)(draw_x + WOUO_FONT_W);
        ++text;
    }
}

uint8_t wouo_text_width_6x8(const char *text)
{
    uint16_t width = 0u;

    if (text == NULL) {
        return 0u;
    }
    while (*text != '\0') {
        width = (uint16_t)(width + WOUO_FONT_W);
        if (width > 255u) {
            return 255u;
        }
        ++text;
    }
    return (uint8_t)width;
}

void wouo_draw_bitmap_rows(uint8_t x, uint8_t y, const uint8_t *bitmap, uint8_t width, uint8_t height, uint8_t on)
{
    uint8_t row_bytes;
    uint8_t row;
    uint8_t col;

    if (bitmap == NULL) {
        return;
    }

    row_bytes = (uint8_t)((width + 7u) / 8u);
    for (row = 0u; row < height; ++row) {
        for (col = 0u; col < width; ++col) {
            uint8_t byte = bitmap[(uint16_t)row * row_bytes + (uint16_t)(col >> 3)];
            uint8_t bit = (uint8_t)(0x80u >> (col & 7u));
            if ((byte & bit) != 0u) {
                ls013_lcd_set_pixel((uint8_t)(x + col), (uint8_t)(y + row), on);
            }
        }
    }
}

void wouo_draw_progress_bar(uint8_t x, uint8_t y, uint8_t w, uint8_t h, uint8_t progress)
{
    uint8_t inner_w;
    uint8_t fill_w;

    ls013_lcd_frame(x, y, w, h, 1u);
    if ((w <= 2u) || (h <= 2u)) {
        return;
    }

    ls013_lcd_rect((uint8_t)(x + 1u), (uint8_t)(y + 1u), (uint8_t)(w - 2u), (uint8_t)(h - 2u), 0u);
    inner_w = (uint8_t)(w - 4u);
    fill_w = (uint8_t)(((uint16_t)inner_w * progress) / 100u);
    if (fill_w != 0u) {
        ls013_lcd_rect((uint8_t)(x + 2u), (uint8_t)(y + 2u), fill_w, (uint8_t)(h - 4u), 1u);
    }
}

uint8_t wouo_anim_i16(int16_t *value, int16_t target, uint8_t weight)
{
    int16_t diff;

    if ((value == NULL) || (weight == 0u)) {
        return 0u;
    }
    diff = (int16_t)(target - *value);
    if ((diff < 2) && (diff > -2)) {
        *value = target;
        return 0u;
    }
    *value = (int16_t)(*value + (diff / (int16_t)weight));
    return 1u;
}
